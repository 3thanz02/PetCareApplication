package com.example.petcareapplication;

public class UserHelperClass {
    // Declare Variables.
    String email;
    //empty helper class
    public UserHelperClass() {
    }
    // Constructor for email.
    public UserHelperClass(String email) {
        this.email = email;
    }
    // Getter for email.
    public String getEmail() {
        return email;
    }
}
